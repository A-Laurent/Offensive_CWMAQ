using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class AmmoComponent : MonoBehaviour
{

    //This component create & set the ammo value to 200

    public int Ammo;
    void Start()
    {
        Ammo = 200;
    }
    void Update()
    {
        if (Ammo <= 0)
            Ammo = 0;
    }
}
